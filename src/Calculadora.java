public class Calculadora {
    /** 
    * Este metodo é utilizado para somar dois numeros inteiros
    * @param numeroUm este é o primeiro parametro do metodo
    * @param numeroDois este é o segundo parametro do metodo
    * @return int o resultado deste método é a soma dos dois numeros. 
    */
    
    public int somar(int numeroUm, int numeroDois){
        return numeroUm + numeroDois;  
    }



}
